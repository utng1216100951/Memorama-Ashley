    /**
     * Metodo para evaluar si un jugador ha encontrado un par
     */
    private void evaluar() {
        if (banInicio && m1.isBanTiro1() && m1.getnSel() == 2) {//No son iguales
            if (!m1.verificar()) {
                tablero[m1.getTiro1()].setIcon(new ImageIcon(getClass().
                        getResource(m1.getNomFondo())));
                tablero[m1.getTiro2()].setIcon(new ImageIcon(getClass().
                        getResource(m1.getNomFondo())));
                tarjetas[m1.getTiro1()].setBackground(new Color(240, 240, 240));
                tarjetas[m1.getTiro2()].setBackground(new Color(240, 240, 240));
                explosion(); ///<<<-----------
            } else {
                txtParesJ1.setText("" + m1.getParesJ1());
                txtParesJ2.setText("" + m1.getParesJ2());
                exclamacion(); //<<------------
                if (m1.Ganador() == 1) {
                    JOptionPane.showMessageDialog(this, "El ganador es: " + m1.
                            getJugador1());
                } else if (m1.Ganador() == 2) {
                    JOptionPane.showMessageDialog(this, "El ganador es: " + m1.
                            getJugador2());
                } else if (m1.Ganador() == 3) {
                    JOptionPane.showMessageDialog(this, "Fue un empate!!!");
                }
            }

        }
    }

    /**
     * Metodo para realizar un tiro, el cual cambia de color el fondo
     * dependiendo el jugador
     *
     * @param pos
     */
    private void tirar(int pos) {
        //pnlTarjeta1.setBackground(Color.yellow);
        if (banInicio) {
            if (m1.isTurnoJ1()) {
                tarjetas[pos].setBackground(Color.blue);
            } else {
                tarjetas[pos].setBackground(Color.red);
            }
            tablero[pos].setIcon(new ImageIcon(getClass().getResource(
                    m1.getTablero(pos))));
        }
    }

    /**
     * Metodo para reproducir un sonido de explocion cuando las tarjetas
     * volteadas no sean pares
     */
    public void explosion() {
        try {
            PlayWave pw1;
            URL url = getClass().getResource("/media/explos.wav");
            pw1 = new PlayWave(url);
            pw1.start();
        } catch (URISyntaxException e) {
            System.out.println("Error en la URL!!");
        }
    }

